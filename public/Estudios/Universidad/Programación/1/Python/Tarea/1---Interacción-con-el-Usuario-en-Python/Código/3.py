numero1 = 2 # crea una variable entera que almacena el número 2
numero2 = 93.3 # crea una variable flotante que almacena en número 93.3

# SUMA
suma = numero1 + numero2 # almacena la suma de los dos números en la variable 'suma'
print(suma) # imprime la variable 'suma'

# RESTA
resta = numero1 - numero2 # almacena la resta entre los dos números en la variable 'resta'
print(resta) # imprime la variable 'resta'

# MULTIPLICACIÓN
multiplicacion = numero1 * numero2 # almacena el producto de los dos números en la variable 'multiplicacion'
print(multiplicacion) # imprime la variable 'multiplicacion'

# DIVISIÓN
division = numero1 / numero2 # almacena el cociente entre los dos números en la variable 'division'
print(division) # imprime la variable 'division'

# MÓDULO
mod = numero1 % numero2 # almacena el resto del cociente entre los dos números en la variable 'mod'
print(mod) # inprime la variable 'mod'
